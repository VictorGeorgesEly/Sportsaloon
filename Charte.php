Voici un exemple de message à éviter :
POUVEZ-VOUS ME DONNER LA REPONSE !!!

Il est également demandé aux utilisateurs de s'appliquer lors de la rédaction de leur message en évitant le plus possible les abréviations (type SMS) et en vérifiant l'orthographe. Un message rédigé en langage abrégé ou difficile à lire compte tenu du nombre de fautes d'orthographe est susceptible d'être supprimé afin de garantir une qualité de lecture des échanges.


